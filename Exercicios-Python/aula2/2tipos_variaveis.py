#variavel integer ou int
numero1 = "15"
numero2 = "1"
n1 = int(numero1)
n2 = int(numero2)

print(f"A soma de {numero1}+{numero2} = {numero1 + numero2}")
print(f"A soma de {n1}+{n2} = {n1 + n2}")


# variavel float = variavel numerica com casas decimais 
resultado = n1/n2
print(f"O resultado é {resultado}")
print(f"O tipo de variavel de resultado é {type(resultado)}")


